package de.aquadiva.joyce.util;

public class OntologyModularizationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2648476155137348338L;

	public OntologyModularizationException() {
		super();
	}

	public OntologyModularizationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OntologyModularizationException(String message, Throwable cause) {
		super(message, cause);
	}

	public OntologyModularizationException(String message) {
		super(message);
	}

	public OntologyModularizationException(Throwable cause) {
		super(cause);
	}

}
